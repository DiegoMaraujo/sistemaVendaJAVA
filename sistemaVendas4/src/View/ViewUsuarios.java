    private void btAterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAterarActionPerformed
        // TODO add your handling code here:
       
        int linha = this.tbUsuario.getSelectedRow();
        int codigoUsuario = (int) this.tbUsuario.getValueAt(linha, 0);
          salvarAlterarUsuario="alterar";
        
        modelUsuario= controllerUsuario.getUsuarioController(codigoUsuario);
            this.txtCod.setText(String.valueOf(modelUsuario.getIdUsuario()));
            this.txtNome.setText(modelUsuario.getUsuNome());
            this.txtLogin.setText(modelUsuario.getUsuLogin());
            this.txtSenha.setText(modelUsuario.getUsuSenha());
       
            this.habilitarDesabilitar(true);
   
       
        
    }//GEN-LAST:event_btAterarActionPerformed

    
    
    private void CarregarUsuario(){
        listaUsuarios = controllerUsuario.getListaUsuarioController();
        DefaultTableModel modelo = (DefaultTableModel)tbUsuario.getModel();
        modelo.setNumRows(0);
       int cont = listaUsuarios.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
               listaUsuarios.get(i).getIdUsuario(),
               listaUsuarios.get(i).getUsuNome(),
               listaUsuarios.get(i).getUsuLogin()
                
            });
        }
    }
    
    private void habilitarDesabilitar(boolean condicao){
        txtNome.setEnabled(condicao);
        txtLogin.setEnabled(condicao);
        txtSenha.setEnabled(condicao);
        btSalvar.setEnabled(condicao);
    }
    private void limparUsuario(){
        txtCod.setText("");
        txtNome.setText("");
        txtLogin.setText("");
        txtSenha.setText("");
    }
    
    
    
    
    
