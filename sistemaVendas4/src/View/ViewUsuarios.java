    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        // TODO add your handling code here:
        try {
            modelUsuario.setIdUsuario(Integer.parseInt(txtCod.getText()));
        } catch (NumberFormatException e) {
        }
        modelUsuario.setUsuNome(txtNome.getText());
        modelUsuario.setUsuLogin(txtLogin.getText());
        modelUsuario.setUsuSenha(txtSenha.getText());   
    
    if(salvarAlterarUsuario == ("salvar")){
        if(controllerUsuario.salvarUsuarioController(modelUsuario)>0){
        JOptionPane.showMessageDialog(this, "Usuario cadastrado com sucesso ");
            CarregarUsuario();
            habilitarDesabilitar(false);
            limparUsuario();
        }else{
              JOptionPane.showMessageDialog(this, "Erro ao cadastrado ");
        }
    }else if(salvarAlterarUsuario == ("alterar")){
          if(controllerUsuario.atualizarUsuarioController(modelUsuario)){
        JOptionPane.showMessageDialog(this, "Usuario Alterado com sucesso ");
            CarregarUsuario();
            habilitarDesabilitar(false);
            limparUsuario();
        }else{
              JOptionPane.showMessageDialog(this, "Erro ao Alterar ");
        }    
        
       }
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        int linha = tbUsuario.getSelectedRow();
        int codigoUsuario = (int)tbUsuario.getValueAt(linha, 0);
        if(controllerUsuario.excluirUsuarioController(codigoUsuario)){
            JOptionPane.showMessageDialog(this, "Usuario Excluido com sucesso");
            CarregarUsuario();
            habilitarDesabilitar(false);
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao Excluido ");
        }
        
    }//GEN-LAST:event_btExcluirActionPerformed

