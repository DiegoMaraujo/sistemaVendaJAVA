    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
       
        
        modelCliente.setCliNome(this.txtNome.getText());
        modelCliente.setCliEndereco(this.txtEndereco.getText());
        modelCliente.setCliBairro(this.txtBairro.getText());
        modelCliente.setCliCidade(this.txtCidade.getText());
        modelCliente.setCliUf(this.txtUf.getSelectedItem().toString());
        modelCliente.setCliCep(this.txtCep.getText());
        modelCliente.setCliTelefone(this.txtTelefone.getText());
        
        
       
        

      if(salvarAlterar == ("salvar")){   
        if(controllerCliente.salvarClienteController(modelCliente)>0){
         JOptionPane.showMessageDialog(this, "Cliente cadastrado com sucesso");
            carregarCliente();
            this.habilitaDesabilitaCampos(false);
            this.limparCampos();
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar");
        }
    }else if(salvarAlterar == ("alterar")){   
        if(controllerCliente.atualizarClienteController(modelCliente)){
            JOptionPane.showMessageDialog(this, "Cliente Alterado com sucesso");
            carregarCliente();
            this.habilitaDesabilitaCampos(false);
            this.limparCampos();
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao Alterar");
        }
       }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linha = tbCliente.getSelectedRow();
        int codigoCliente = (int) tbCliente.getValueAt(linha, 0);
        if(controllerCliente.excluirClienteController(codigoCliente)){
            JOptionPane.showMessageDialog(this, "Cliente Excluido com sucesso");
            this.carregarCliente();
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao excluir");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        int linha = tbCliente.getSelectedRow();
        int codigoCliente = (int) tbCliente.getValueAt(linha, 0);
        salvarAlterar = "alterar";
        
        modelCliente = controllerCliente.getClienteController(codigoCliente);
        txtCod.setText(String.valueOf(modelCliente.getIdCliente()));
        txtNome.setText(modelCliente.getCliNome());
        txtEndereco.setText(modelCliente.getCliEndereco());
        txtBairro.setText(modelCliente.getCliBairro());
        txtCidade.setText(modelCliente.getCliCidade());
        txtUf.setSelectedItem(modelCliente.getCliUf());
        txtCep.setText(modelCliente.getCliCep());
        txtTelefone.setText(modelCliente.getCliTelefone());
        this.habilitaDesabilitaCampos(true);
        
    }//GEN-LAST:event_btnAlterarActionPerformed

