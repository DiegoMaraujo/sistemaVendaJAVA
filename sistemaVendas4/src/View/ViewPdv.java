    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here:
        int quantidadeLinha = tblPdvVendas.getRowCount();
        if(quantidadeLinha < 1){
            JOptionPane.showMessageDialog(this, "Nenhum item na lista");
        }else{
            DefaultTableModel modelo = (DefaultTableModel)tblPdvVendas.getModel();
            int linha = Integer.parseInt(JOptionPane.showInputDialog("Informe o item a ser Excluido "));
            modelo.removeRow(linha -1);
            txtValorBruto.setText(String.valueOf(somarTotal()));
            for (int i = 0; i < quantidadeLinha; i++) {
                modelo.setValueAt(i +1 , i, 0);
            }
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

