    private void salvarVendas(){
            // TODO add your handling code here:
        int cont;
        int codigoProduto = 0, codigoVenda = 0;
       modelVendas = new ModelVenda();
       modelVendas.setCliente(1);
       
        try {
            modelVendas.setVenDateVenda(blDatas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));
        } catch (Exception ex) {
            Logger.getLogger(ViewPdv.class.getName()).log(Level.SEVERE, null, ex);
        }
        //salva vendas
        modelVendas.setVenValorLiquido(blMascara.arredondamentoComPontoDuasCasasFDouble(viewPagamentoPdv.getValorTotal()));
        modelVendas.setVenValorDesconto(viewPagamentoPdv.getDesconto());
        modelVendas.setVenValorBruto(Double.parseDouble(txtValorBruto.getText()));
       
        codigoVenda = controllerVenda.salvarVendaController(modelVendas);
        
        cont = tblPdvVendas.getRowCount();
        for (int i = 0; i <cont; i++) {
            codigoProduto = (int) tblPdvVendas.getValueAt(i, 1); 
            modelVendasProdutos = new ModelVendasProdutos();
            modelProdutos = new ModelProdutos();
                // produtos vendas
                modelVendasProdutos.setProduto(codigoProduto);
                modelVendasProdutos.setVendas(codigoVenda );
                modelVendasProdutos.setVenProValor((double) tblPdvVendas.getValueAt(i, 4));
                modelVendasProdutos.setVenProQuantidade(Integer.parseInt(tblPdvVendas.getValueAt(i, 3).toString()));
                //produto
                modelProdutos.setIdProduto(codigoProduto);
                modelProdutos.setProEstoque(controllerProdutos.retornarProdutoController(codigoProduto).getProEstoque()
                        - Integer.parseInt(tblPdvVendas.getValueAt(i, 3).toString()));
                listaModelVendasProdutoses.add(modelVendasProdutos);
                listaModelProdutos.add(modelProdutos);
        }
       //salvar todos produtos da venda
        if(controllerVendasProdutos.salvarVendasProdutosController(listaModelVendasProdutoses)){
            controllerProdutos.alterarEstoqueProdutoController(listaModelProdutos);
              JOptionPane.showMessageDialog(this, "Venda Produto salva com sucesso");
              limparTela();
            
        }else{
            JOptionPane.showMessageDialog(this, "ERRO ao salva Produto");
        }

    }
    
