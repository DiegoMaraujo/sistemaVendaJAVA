    private void limparTela(){
    txtValorBruto.setText("");
        DefaultTableModel modelo = (DefaultTableModel) tblPdvVendas.getModel();
        modelo.setNumRows(0);
        lblStatus.setText("Caixa Livre");
    
    
    }
    
    private void carregarConteudo(java.awt.event.KeyEvent e){  
        lblStatus.setText("Venda em aberto");
        
    DefaultTableModel model = (DefaultTableModel)tblPdvVendas.getModel();
        if(e.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER ){
            try {
            modelProdutos = controllerProdutos.retornarProdutoController(Integer.parseInt(txtCodPdvProdutos.getText()));
                model.addRow(new Object[]{
                    model.getRowCount()+1,
                    modelProdutos.getIdProduto(),
                    modelProdutos.getProNome(),
                    quantidade,
                    modelProdutos.getProValor(),
                    modelProdutos.getProValor() * quantidade
   
                }); 
            txtValorBruto.setText(String.valueOf(somarTotal()));
            txtCodPdvProdutos.setText("");
            quantidade = 1;      
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "ERRO digite um item ","Erro",JOptionPane.ERROR_MESSAGE);
                
            }

        }
        
    }
    private float somarTotal(){
    float soma = 0, valor = 0;
    int cont = tblPdvVendas.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = Float.parseFloat(String.valueOf(tblPdvVendas.getValueAt(i, 5)));
            soma = soma + valor;
        }
    return soma;
    }
    
